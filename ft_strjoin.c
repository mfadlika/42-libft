#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	s1_len;
	size_t	s2_len;
	size_t	total_len;
	char	*new_s;

	if (!s1 && !s2)
		return (ft_strdup(""));
	if (s1 && !s2)
		return (ft_strdup(s1));
	if (!s1 && s2)
		return (ft_strdup(s2));
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	total_len = s1_len + s2_len + 1;
	new_s = malloc(sizeof(char) * total_len);
	if (!new_s)
		return (0);
	ft_memmove(new_s, s1, s1_len);
	ft_memmove(new_s + s1_len, s2, s2_len);
	new_s[total_len - 1] = '\0';
	return (new_s);
}
