#include "libft.h"

int	ft_isalpha(int c)
{
	while (c != '\0')
	{
		if (c >= 97 && c <= 122)
			return (1);
		else if (c >= 65 && c <= 90)
			return (1);
		else
			return (0);
	}
	return (0);
}
